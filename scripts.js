	$(document).scroll(function() {
		var y = $(this).scrollTop();
		if (y > 100) {
			$('.arrow').fadeIn();
		} else {
			$('.arrow').fadeOut();
		}
	});


	$(document).ready( function() {
    $("#labels-1").hide(); //hide your div initially
    var topOfOthDiv = $("#trigger-1").offset().top;
    $(window).scroll(function() {
        if($(window).scrollTop() > topOfOthDiv) { //scrolled past the other div?
            $("#labels-1").fadeIn(); //reached the desired point -- show div
        }
        else{
            $("#labels-1").fadeOut(); //else above the desired point -- hide div
        }
    });
});

